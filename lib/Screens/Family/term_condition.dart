  Widget _buildSectionTitle(BuildContext context, String title) {
    return Container(
      padding: const EdgeInsets.all(8.0),
      margin: const EdgeInsets.only(top: 12),
      child: Text(
        title,
        style: AppStyles.styleBold(18, context),
      ),
    );
  }

  Widget _buildParagraph(BuildContext context, String text) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 4.0),
      child: Text(
        text,
        textAlign: TextAlign.justify,
        style: AppStyles.styleMedium(14, context),
      ),
    );
  }

  Widget _buildBulletPoint(BuildContext context, String text) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 2.0, horizontal: 8.0),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            "• ",
            style: AppStyles.styleRegular(14, context),
          ),
          Expanded(
            child: Text(
              text,
              textAlign: TextAlign.justify,
              style: AppStyles.styleRegular(14, context),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildSubPoint(BuildContext context, String text) {
    return Padding(
      padding: const EdgeInsets.symmetric(vertical: 2.0, horizontal: 8.0),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text("   -  ", style: AppStyles.styleRegular(14, context)),
          Expanded(
            child: Text(
              text,
              textAlign: TextAlign.justify,
              style: AppStyles.styleRegular(14, context),
            ),
          ),
        ],
      ),
    );
  }
